public class DataType {
    public static void main(String[] args) {
        
        int a=5;
        float b=5.0f;
        double d=75.0;
        char c='a';
        boolean g=true;
        System.out.println(+a);
        System.out.println(b);
        System.out.println(d);
        System.out.println(c);
        System.out.println(g);
    }
}
